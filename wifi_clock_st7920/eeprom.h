const int HOL_COUNT = 365;

static void epochToMD(unsigned long ep, int tz, int& mo, int& dy) {
  // signed arithmetic is required for negative timezones
  long long localEp = (long long)ep + (long long)tz * 3600LL;
  if (localEp < 0) localEp = 0;
  unsigned long z   = (unsigned long)(localEp / 86400LL) + 719468UL;
  unsigned long era = z / 146097UL;
  unsigned long doe = z - era * 146097UL;
  unsigned long yoe = (doe - doe/1460 + doe/36524 - doe/146096) / 365;
  unsigned long doy = doe - (365*yoe + yoe/4 - yoe/100);
  unsigned long mp  = (5*doy + 2) / 153;
  dy = (int)(doy - (153*mp + 2)/5 + 1);
  mo = (int)(mp < 10 ? mp + 3 : mp - 9);
}

const char* getTodayHoliday() {
  unsigned long ep = timeClient.getEpochTime();
  if (ep < 1000000000UL) return "";
  int m, day;
  epochToMD(ep, cfg.timezone, m, day);
  for (int i = 0; i < HOL_COUNT; i++)
    if (HOLIDAYS[i].month == m && HOLIDAYS[i].day == day)
      return HOLIDAYS[i].name;
  return "";
}

void saveConfig() {
  EEPROM.begin(sizeof(Config));
  EEPROM.put(0, cfg);
  EEPROM.commit();
  EEPROM.end();
}
void loadConfig() {
  EEPROM.begin(sizeof(Config));
  Config tmp;
  EEPROM.get(0, tmp);
  EEPROM.end();
  if (strcmp(tmp.magic, "CF3") == 0) cfg = tmp;
}
